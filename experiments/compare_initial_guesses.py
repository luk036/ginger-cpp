# ─── Experiment 1: Angle distribution ────────────────────────────────────────

def exp1_angle_distribution() -> None:
    """Show that LDS and naive generate DIFFERENT sets of angles."""
    print("=" * 72)
    print("Experiment 1: Initial guess angle distribution")
    print("=" * 72)

    for degree in [4, 8, 12, 16]:
        lds_angles = angles_from_lds(degree)
        naive_angles = angles_from_naive(degree)

        lds_sorted = sorted(lds_angles)
        naive_sorted = sorted(naive_angles)

        # Quantify difference: sum of absolute differences in sorted order
        diff = sum(abs(a - b) for a, b in zip(lds_sorted, naive_sorted))
        max_gap_lds = max(
            lds_sorted[i + 1] - lds_sorted[i] for i in range(len(lds_sorted) - 1)
        )
        max_gap_naive = max(
            naive_sorted[i + 1] - naive_sorted[i]
            for i in range(len(naive_sorted) - 1)
        )

        print(f"\ndegree={degree}:")
        print(f"  LDS   sorted angles (deg): {[f'{a*180/math.pi:.0f}' for a in lds_sorted]}")
        print(f"  Naive sorted angles (deg): {[f'{a*180/math.pi:.0f}' for a in naive_sorted]}")
        print(f"  Total angular difference:  {diff:.4f} rad")
        print(f"  Max gap LDS:  {max_gap_lds * 180 / math.pi:.1f}°")
        print(f"  Max gap naive: {max_gap_naive * 180 / math.pi:.1f}°")
        sets_ident = all(
            abs(a - b) < 1e-10 for a, b in zip(lds_sorted, naive_sorted)
        )
        print(f"  Sets are identical: {sets_ident}")

    # Show the prefix coverage property
    print("\n\nPrefix coverage (degree=8):")
    print(f"{'k':>3} | {'LDS k-th angle':>16} | {'largest gap':>12}")
    print("-" * 45)
    lds8 = angles_from_lds(8)
    for k in range(1, 9):
        pts = lds8[:k]
        pts_sorted = sorted(pts)
        gaps = [pts_sorted[i+1] - pts_sorted[i] for i in range(len(pts_sorted)-1)]
        if gaps:
            max_gap = max(gaps) * 180 / math.pi
        else:
            max_gap = 360.0
        print(f"{k:3d} | {lds8[k-1]*180/math.pi:7.1f}° → {lds8[k-1]*180/math.pi:7.1f}° | {max_gap:7.1f}°")


# ─── Experiment 2: ST convergence comparison ─────────────────────────────────

def get_tolerance(coeffs: List[float]) -> float:
    """Pick tolerance based on polynomial degree (higher degree = looser)."""
    deg = len(coeffs) - 1
    if deg >= 40:
        return 1e-8
    if deg >= 20:
        return 1e-10
    return 1e-12


def run_st_with_guess(
    coeffs: List[float],
    guess_fn: Callable,
    name: str,
    tol: float | None = None,
    max_iters: int = 2000,
) -> Tuple[int, bool]:
    if tol is None:
        tol = get_tolerance(coeffs)
    """Run Aberth ST path with given initial guess function."""
    zs = guess_fn(coeffs)
    opt = Options()
    opt.tolerance = tol
    opt.max_iters = max_iters
    _, niter, found = aberth(coeffs, zs, opt)
    return niter, found


def exp2_st_convergence() -> None:
    """Compare ST convergence: LDS vs naive."""
    print("\n" + "=" * 72)
    print("Experiment 2: ST path convergence (LDS vs naive)")
    print("=" * 72)

    for pname, coeffs in ALL_POLYS:
        n_lds, found_lds = run_st_with_guess(coeffs, initial_aberth, "LDS")
        n_naive, found_naive = run_st_with_guess(
            coeffs, initial_aberth_orig, "naive"
        )
        status = "[OK]" if found_lds else "[FAIL]"
        status_n = "[OK]" if found_naive else "[FAIL]"
        print(f"\n{pname}:")
        print(f"  LDS:   {n_lds:4d} iters {status}")
        print(f"  Naive: {n_naive:4d} iters {status_n}")
        if found_lds and found_naive:
            diff = n_naive - n_lds
            if diff > 0:
                print(f"  ➜ LDS {diff} iters FASTER ({diff/n_naive*100:.0f}%)")
            elif diff == 0:
                print(f"  ➜ Same iterations")
            else:
                print(f"  ➜ Naive {-diff} iters FASTER")


# ─── Experiment 3: Ordering effect (shuffle test) ────────────────────────────

def run_st_with_ordered_guesses(
    coeffs: List[float],
    zs: List[complex],
    tol: float | None = None,
    max_iters: int = 2000,
) -> Tuple[int, bool]:
    """Run Aberth ST path with given initial guess list."""
    if tol is None:
        tol = get_tolerance(coeffs)
    opt = Options()
    opt.tolerance = tol
    opt.max_iters = max_iters
    _, niter, found = aberth(coeffs, zs[:], opt)
    return niter, found


def exp3_ordering_effect() -> None:
    """Test whether the ORDER within the guess vector matters for ST path."""
    print("\n" + "=" * 72)
    print("Experiment 3: Ordering effect (shuffle test)")
    print("=" * 72)

    for pname, coeffs in ALL_POLYS:
        # Get LDS guesses
        zs_lds = initial_aberth(coeffs)

        # Shuffle 10 times and measure variation
        iters_original, found_original = run_st_with_ordered_guesses(
            coeffs, zs_lds
        )
        if not found_original:
            print(f"\n{pname}: did not converge, skipping shuffle test")
            continue

        shuffle_results = []
        random.seed(42)
        for trial in range(10):
            zs_shuffled = zs_lds[:]
            random.shuffle(zs_shuffled)
            n, f = run_st_with_ordered_guesses(coeffs, zs_shuffled)
            shuffle_results.append((n, f))

        converged_shuffles = [n for n, f in shuffle_results if f]
        print(f"\n{pname}:")
        print(f"  Original LDS order: {iters_original} iters")
        if converged_shuffles:
            print(
                f"  Shuffled (10 trials): "
                f"min={min(converged_shuffles)}, "
                f"max={max(converged_shuffles)}, "
                f"mean={statistics.mean(converged_shuffles):.1f}"
            )
            worst_case = max(converged_shuffles) - iters_original
            print(f"  ➜ Worst shuffle slowdown: {worst_case} iters "
                  f"({worst_case/max(converged_shuffles)*100:.0f}% slower)")
        else:
            print(f"  Shuffled: {sum(1 for _, f in shuffle_results if not f)}/10 did not converge")

        # Also test: sort LDS by angle (naive ordering, LDS set)
        zs_sorted = sorted(zs_lds, key=lambda z: math.atan2(z.imag, z.real))
        n_sorted, f_sorted = run_st_with_ordered_guesses(coeffs, zs_sorted)
        print(f"  LDS set sorted by angle: {n_sorted} iters {'[OK]' if f_sorted else '[FAIL]'}")


