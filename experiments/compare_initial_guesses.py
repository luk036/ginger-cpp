def angles_from_lds(n: int) -> List[float]:
    """Extract the angles generated by the LDS Circle sequence (base 2)."""
    # Replicate the lds::Circle<2> + vdC logic
    angles = []
    for k in range(1, n + 1):
        # van der Corput base 2
        v = 0.0
        denom = 1.0
        cnt = k
        while cnt:
            cnt, rem = divmod(cnt, 2)
            denom *= 2.0
            v += rem / denom
        angles.append(v * 2.0 * math.pi)
    return angles


def angles_from_naive(n: int) -> List[float]:
    """Generate naive equally-spaced angles with 0.25 offset."""
    angle_step = 2.0 * math.pi / n
    return [angle_step * (0.25 + i) for i in range(n)]


